// ============================================================
// 📘 04 - Break & Continue
// ============================================================

// break: loop সম্পূর্ণ বন্ধ করে দেয়
// continue: বর্তমান iteration skip করে পরেরটায় যায়

// ============================================================
// 📌 break
// ============================================================
for (let i = 1; i <= 10; i++) {
  if (i === 5) break; // 5 এ পৌঁছালে loop বন্ধ
  console.log(i);
}
// 1, 2, 3, 4

// Array তে search করা:
const numbers = [3, 7, 1, 9, 4, 6, 2];
let found = -1;
for (let i = 0; i < numbers.length; i++) {
  if (numbers[i] === 9) {
    found = i;
    break; // পেলেই থামো, বাকি দেখার দরকার নেই
  }
}
console.log(found === -1 ? "পাওয়া যায়নি" : `Index: ${found}`);

// ============================================================
// 📌 continue
// ============================================================
for (let i = 1; i <= 10; i++) {
  if (i % 2 === 0) continue; // জোড় হলে skip
  console.log(i); // শুধু বিজোড়: 1, 3, 5, 7, 9
}

// ============================================================
// 📌 Labeled Loops (nested loop break করার জন্য)
// ============================================================
outer: for (let i = 0; i < 3; i++) {
  for (let j = 0; j < 3; j++) {
    if (j === 1) break outer; // বাইরের loop ও break হয়
    console.log(i, j);
  }
}
// 0 0  (শুধু এটুকু)


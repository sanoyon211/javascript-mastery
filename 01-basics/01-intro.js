// ✅ প্রথম JavaScript Program
console.log("Hello, World!"); // Output: Hello, World!
console.log("আমি JavaScript শিখছি!"); // বাংলাও চলে!

// ============================================================
// 📌 console এর বিভিন্ন method
// ============================================================

console.log("সাধারণ message");        // সবচেয়ে বেশি ব্যবহার হয়
console.warn("এটা একটা warning!");    // হলুদ রঙে দেখায়
console.error("এটা একটা error!");     // লাল রঙে দেখায়
console.info("এটা info");             // তথ্য দেখানোর জন্য
console.table([1, 2, 3]);             // table আকারে দেখায়

// একসাথে অনেক কিছু print করা
console.log("নাম:", "Rahim", "বয়স:", 20);

